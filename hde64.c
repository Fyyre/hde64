unsigned int hde64_disasm(const void* code, hde64s* hs)
{
	const uint8_t* p = (const uint8_t*)code;
	uint8_t c, pref = 0, opcode, cflags;
	const uint8_t* ht = hde64_table;
	uint8_t m_mod, m_reg, m_rm, disp_size = 0;
	uint8_t op64 = 0;

	memset(hs, 0, sizeof(hde64s));

	/* ====================== PREFIXES ====================== */
	for (int x = 16; x > 0; x--) {
		c = *p++;
		switch (c) {
		case 0xf3: hs->p_rep = c; pref |= 0x04; break;
		case 0xf2: hs->p_rep = c; pref |= 0x02; break;
		case 0xf0: hs->p_lock = c; pref |= 0x20; break;
		case 0x26: case 0x2e: case 0x36: case 0x3e: case 0x64: case 0x65:
			hs->p_seg = c; pref |= 0x40; break;
		case 0x66: hs->p_66 = c; pref |= 0x08; break;
		case 0x67: hs->p_67 = c; pref |= 0x10; break;
		default: goto pref_done;
		}
	}

pref_done:
	hs->flags = (uint32_t)pref << 23;
	if (pref == 0) pref = 0x01;

	/* ====================== REX + VEX (AVX/AVX2) ====================== */
	c = *p;

	if ((c & 0xF0) == 0x40) {                       // REX prefix
		hs->rex = c;
		hs->rex_w = (c >> 3) & 1;
		hs->rex_r = (c >> 2) & 1;
		hs->rex_x = (c >> 1) & 1;
		hs->rex_b = c & 1;
		c = *p++;
	}
	else if (c == 0xC4 || c == 0xC5) {             // VEX prefix (AVX / AVX2)
		hs->flags |= 0x00020000;                    // Mark as VEX instruction
	}

	hs->opcode = c = *p++;

	/* ====================== OPCODE ====================== */
	if (c == 0x0F) {
		hs->opcode2 = c = *p++;
		ht += 0x4A;
	}
	else if (c >= 0xA0 && c <= 0xA3) {
		op64 = 1;
		if (pref & 0x10) pref |= 0x08; else pref &= ~0x08;
	}

	opcode = c;
	cflags = ht[ht[opcode / 4] + (opcode % 4)];

	if (cflags == C_ERROR) {
		hs->flags |= F_ERROR | F_ERROR_OPCODE;
		cflags = 0;
	}

	if (cflags & C_GROUP) {
		uint16_t t = *(const uint16_t*)(ht + (cflags & 0x7F));
		cflags = (uint8_t)t;
	}

	/* ====================== MODRM ====================== */
	if (cflags & C_MODRM) {
		hs->flags |= F_MODRM;
		hs->modrm = c = *p++;
		hs->modrm_mod = m_mod = c >> 6;
		hs->modrm_reg = m_reg = (c >> 3) & 7;
		hs->modrm_rm = m_rm = c & 7;

		if (m_mod != 3 && m_rm == 4) {
			hs->flags |= F_SIB;
			hs->sib = c = *p++;
			hs->sib_scale = c >> 6;
			hs->sib_index = (c >> 3) & 7;
			hs->sib_base = c & 7;
		}

		switch (m_mod) {
		case 0: if (m_rm == 5 || (pref & 0x10 && m_rm == 6)) disp_size = 4; break;
		case 1: disp_size = 1; break;
		case 2: disp_size = (pref & 0x10) ? 2 : 4; break;
		}

		if (disp_size) {
			hs->disp.disp32 = *(const uint32_t*)p;
			if (disp_size == 1) {
				hs->flags |= F_DISP8;
				hs->disp.disp8 = *p;
			}
			else if (disp_size == 2) {
				hs->flags |= F_DISP16;
				hs->disp.disp16 = *(const uint16_t*)p;
			}
			else {
				hs->flags |= F_DISP32;
			}
			p += disp_size;
		}
	}

	/* ====================== IMMEDIATES ====================== */
	if (cflags & C_IMM_P66) {
		if (op64 || !(pref & 0x08)) {
			hs->flags |= F_IMM64;
			hs->imm.imm64 = *(const uint64_t*)p;
			p += 8;
		}
		else {
			hs->flags |= F_IMM32;
			hs->imm.imm32 = *(const uint32_t*)p;
			p += 4;
		}
	}
	if (cflags & C_IMM16) {
		hs->flags |= F_IMM16;
		hs->imm.imm16 = *(const uint16_t*)p;
		p += 2;
	}
	if (cflags & C_IMM8) {
		hs->flags |= F_IMM8;
		hs->imm.imm8 = *p++;
	}
	if (cflags & C_REL32) {
		hs->flags |= F_IMM32 | F_RELATIVE;
		hs->imm.imm32 = *(const uint32_t*)p;
		p += 4;
	}
	else if (cflags & C_REL8) {
		hs->flags |= F_IMM8 | F_RELATIVE;
		hs->imm.imm8 = *p++;
	}

	hs->len = (uint8_t)(p - (const uint8_t*)code);

	if (hs->len > 15) {
		hs->flags |= F_ERROR | F_ERROR_LENGTH;
		hs->len = 15;
	}

	return hs->len;
}